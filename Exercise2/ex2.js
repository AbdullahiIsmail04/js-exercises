// Comparison Operators
let a = 15;
let b = "15";

// -> Equal to operator
console.log(a == b);

// -> Strictly Equal to operator
console.log(a === b);

// -> Not Equal to operator
console.log(a != b);

// -> Strictly Not Equal to operator
console.log(a !== b);

// -> Greater than operator
console.log(a > b);

// -> Less than operator
console.log(a < b);

// -> Greater than or equal to operator
console.log(a >= b);

// -> Less than or equal to operator
console.log(a <= b);