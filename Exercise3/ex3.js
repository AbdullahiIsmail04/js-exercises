// Logical Operators
let isSunny = true;
let isWeekend = true;

// Using logical AND (&&)
console.log("Using logical AND (&&):");
console.log(isSunny && isWeekend);
console.log("You can go to the beach!");

// Using logical OR (||)
console.log("\nUsing logical OR (||):");
console.log(isSunny || isWeekend);
console.log("You can go to the beach!");

// Using logical NOT (!)
console.log("\nUsing logical NOT (!):");
console.log(!isSunny);
console.log("You cannot go to the beach!");
